void MainWindow::testK1(QString str)
{
    double n=str.length();
    double v0=0;
    for (int i=0; i<n; i++)
    {
        QString ch=str.at(i);
        if (ch.toInt()==0) v0++;
    }
    double v1=n-v0;

    double min=(n-2.58*sqrt(n))/2;
    double max=(n+2.58*sqrt(n))/2;

    bool random=false;
    if (min<v0&&min<v1&&max>v0&&max>v1) random=true;

    if (random) ui->label_17->setText("<html><head/><body><p align='center'>"
                                      "<span style=' color:#00f400;'>+"
                                      "</span></p></body></html>");
    else ui->label_17->setText("<html><head/><body><p align='center'>"
                               "<span style=' color:#ec0000;'>-"
                               "</span></p></body></html>");


    ui->listWidget_2->item(2)->setText("0:"+QString::number(min,'f',0)+"<"
                                       +QString::number(v0)+"<"+
                                       QString::number(max,'f',0));
    ui->listWidget_2->item(3)->setText("1:"+QString::number(min,'f',0)+"<"
                                       +QString::number(v1)+"<"+
                                       QString::number(max,'f',0));
}

void MainWindow::testK2(QString str)
{
    int n=str.length();

    if (str.length()<2) return;


    int dlina_fragmenta=0;
    int need_razmer=0;
    int full_razmer=0;
    int max_coord=0;
    for (int i=1; i<16; i++)
    {
        max_coord=pow(2,i);
        full_razmer=pow(max_coord,2);
        need_razmer=(double)n/i+0.5;
        if (need_razmer<=full_razmer)
        {
            dlina_fragmenta=i;
            i=16;
        }
    }
    if (dlina_fragmenta==0) return;
    int lishnie=full_razmer-need_razmer;


    //задаем размер изображения, необходимый для шкалы
    QImage image(max_coord,max_coord,
                 QImage::Format_ARGB32_Premultiplied);
    //заполняем прозрачными пикселями, чтобы не было мусорных цветов
    QRgb chisto=0x00000000;
    image.fill(chisto);



    int stop_pos=0;
    for (int i=0; i<=n-2*dlina_fragmenta; i+=dlina_fragmenta)
    {
        int coord_x=0;
        int coord_y=0;
        for (int j=0; j<dlina_fragmenta; j++)
        {
            QString buf_x=str.at(i+j);
            QString buf_y=str.at(i+j+dlina_fragmenta);
            coord_x+=buf_x.toInt()*pow(2,(dlina_fragmenta-1)-j);
            coord_y+=buf_y.toInt()*pow(2,(dlina_fragmenta-1)-j);
        }
        image.setPixel(coord_x,coord_y, 0xFF000000);
        stop_pos=i+dlina_fragmenta;
    }

    //неполные пограничные координаты между концом и началом
    for (int i=stop_pos; i<n; i+=dlina_fragmenta)
    {
        int coord_x=0;
        int coord_y=0;
        for (int j=0; j<dlina_fragmenta; j++)
        {
            QString buf_x;
            QString buf_y;
            if (i+j<n) buf_x=str.at(i+j);
            else buf_x=str.at(i+j-n);
            if (i+j+dlina_fragmenta<n) buf_y=str.at(i+j+dlina_fragmenta);
            else buf_y=str.at(i+j+dlina_fragmenta-n);
            coord_x+=buf_x.toInt()*pow(2,(dlina_fragmenta-1)-j);
            coord_y+=buf_y.toInt()*pow(2,(dlina_fragmenta-1)-j);
        }
        image.setPixel(coord_x,coord_y, 0xFF000000);
    }



    int white=0;
    for (int i=0; i<max_coord; i++)
        for (int j=0; j<max_coord; j++)
            if (image.pixel(i,j)==0x00000000) white++;

    double propysk=white-lishnie;
    double black=full_razmer-white;
    double mera_xaosa=(black/(black+propysk))*100;

    //добавляем изображение на сцену
    grafic->clear();
    QPixmap pixmap;
    grafic->addPixmap(pixmap.fromImage(image));
    grafic->setSceneRect(0,0,max_coord,max_coord);

    ui->graphicsView->setScene(grafic);
    ui->graphicsView->fitInView(grafic->sceneRect(),Qt::KeepAspectRatio);





    ui->label_18->setText("<html><head/><body><p align='center'>"+
                          QString::number(mera_xaosa,'f',2)+
                          "%</p></body></html>");


    ui->listWidget_2->item(0)->setText("XY: "+QString::number(dlina_fragmenta));
    ui->listWidget_2->item(1)->setText("Лишние: "+QString::number(lishnie));
}

